#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*s_1;
	unsigned char	*s_2;

	s_1 = (unsigned char *)s1;
	s_2 = (unsigned char *)s2;
	i = 0;
	while (i < n && s_1[i] == s_2[i])
		i++;
	if (n == i)
		return (0);
	return (s_1[i] - s_2[i]);
}
